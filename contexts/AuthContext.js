// Contexto de autentica√ß√£o
import React, { createContext, useContext, useState, useEffect } from 'react';
import { AuthCookies, FriendsCookies } from '../utils/cookies';
import authDiagnostic, { logAuth } from '../utils/authDiagnostic';

const AuthContext = createContext();

export const useAuth = () => {
  const context = useContext(AuthContext);
  if (!context) {
    throw new Error('useAuth deve ser usado dentro de um AuthProvider');
  }
  return context;
};

export const AuthProvider = ({ children }) => {
  const [user, setUser] = useState(null);
  const [isLoading, setIsLoading] = useState(true);
  const [isAuthenticated, setIsAuthenticated] = useState(false);

  // Verificar se h√° sess√£o ativa ao carregar
  useEffect(() => {
    if (typeof window !== 'undefined') {
      checkSession();
    }
  }, []);

  // Verifica√ß√£o peri√≥dica de sa√∫de da autentica√ß√£o (a cada 5 minutos)
  useEffect(() => {
    if (!isAuthenticated || typeof window === 'undefined') return;

    const healthCheck = setInterval(async () => {
      try {
        const diagnostic = await authDiagnostic.runFullDiagnostic();

        // Se detectou problemas cr√≠ticos, tentar corrigir
        if (diagnostic.commonIssues.some(issue => issue.severity === 'high')) {
          logAuth('warning', 'Problemas cr√≠ticos detectados na autentica√ß√£o');

          // Se token inv√°lido, tentar renovar
          if (!diagnostic.tokenValidity.valid) {
            const renewResult = await renewToken();
            if (!renewResult.success) {
              logAuth('error', 'Falha na renova√ß√£o autom√°tica do token');
            }
          }
        }
      } catch (error) {
        logAuth('error', 'Erro na verifica√ß√£o de sa√∫de da autentica√ß√£o', error.message);
      }
    }, 5 * 60 * 1000); // 5 minutos

    return () => clearInterval(healthCheck);
  }, [isAuthenticated]);

  // Fun√ß√£o para verificar sess√£o existente
  const checkSession = async () => {
    try {
      logAuth('info', 'Verificando sess√£o existente');

      // Tentar obter token dos cookies primeiro, depois localStorage
      const sessionToken = AuthCookies.getSessionToken();

      if (!sessionToken) {
        logAuth('warning', 'Nenhum token de sess√£o encontrado');
        setIsLoading(false);
        return;
      }

      logAuth('info', `Token encontrado (${sessionToken.length} chars)`);
      authDiagnostic.syncStorage(); // Sincronizar storage
      const response = await fetch(`/api/auth?sessionToken=${sessionToken}`, {
        method: 'GET',
        headers: {
          'Cache-Control': 'no-cache',
          'Pragma': 'no-cache'
        }
      });

      if (response.ok) {
        const data = await response.json();
        setUser(data.user);
        setIsAuthenticated(true);

        // Atualizar cookies e localStorage com dados mais recentes se necess√°rio
        const currentUserData = JSON.stringify(data.user);
        const savedUserData = AuthCookies.getUserData();
        if (!savedUserData || JSON.stringify(savedUserData) !== currentUserData) {
          AuthCookies.saveAuth(sessionToken, data.user, AuthCookies.shouldRemember());
        }
      } else {
        // Sess√£o inv√°lida - tratar baseado no tipo de erro
        const errorData = await response.json().catch(() => ({}));

        if (response.status === 401) {
          // Verificar se √© erro de token expirado vs inv√°lido
          if (errorData.error === 'Sess√£o expirada') {
            // Token expirado - tentar renovar se poss√≠vel
            const savedUserData = AuthCookies.getUserData();
            if (savedUserData) {
              // Manter dados do usu√°rio mas marcar como n√£o autenticado
              setUser(savedUserData);
              setIsAuthenticated(false);
              console.log('üîÑ Sess√£o expirada - necess√°rio relogin');
            } else {
              AuthCookies.clearAuth();
              setUser(null);
              setIsAuthenticated(false);
            }
          } else {
            // Token inv√°lido ou outros erros 401 - limpar tudo
            AuthCookies.clearAuth();
            setUser(null);
            setIsAuthenticated(false);
            console.log('‚ùå Sess√£o inv√°lida - dados limpos');
          }
        } else {
          // Erro de rede ou servidor - manter dados locais
          const savedUserData = AuthCookies.getUserData();
          if (savedUserData) {
            setUser(savedUserData);
            setIsAuthenticated(true);
            console.log('üîÑ Erro de rede - usando dados locais');
          }
        }
      }
    } catch (error) {
      // Em caso de erro de rede, tentar carregar dados dos cookies/localStorage
      const savedUserData = AuthCookies.getUserData();
      if (savedUserData) {
        try {
          setUser(savedUserData);
          setIsAuthenticated(true);
        } catch (e) {
          AuthCookies.clearAuth();
        }
      }
    } finally {
      setIsLoading(false);
    }
  };

  // Fun√ß√£o para registrar usu√°rio
  const register = async (username, password, email = null, anonymousUserId = null) => {
    try {
      // Capturar c√≥digo de refer√™ncia da URL se existir
      const urlParams = new URLSearchParams(window.location.search);
      const referralCode = urlParams.get('ref');

      const response = await fetch('/api/auth', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        },
        body: JSON.stringify({
          action: 'register',
          username,
          password,
          email,
          anonymousUserId,
          referralCode
        })
      });

      const data = await response.json();

      if (response.ok) {
        // Salvar token de sess√£o e dados do usu√°rio nos cookies e localStorage
        AuthCookies.saveAuth(data.sessionToken, data.user, true); // Sempre lembrar no registro
        setUser(data.user);
        setIsAuthenticated(true);

        // Disparar evento customizado para notificar outros componentes sobre o registro
        window.dispatchEvent(new CustomEvent('userLoggedIn', {
          detail: { user: data.user }
        }));

        // Mostrar mensagem de referral se houver
        if (data.referralMessage) {
          setTimeout(() => {
            alert(`üéâ ${data.referralMessage}`);
          }, 1000);
        }

        return { success: true, user: data.user };
      } else {
        return { success: false, error: data.error };
      }
    } catch (error) {
      return { success: false, error: 'Erro de conex√£o' };
    }
  };

  // Fun√ß√£o para fazer login
  const login = async (username, password, rememberMe = true) => {
    try {
      const response = await fetch('/api/auth', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        },
        body: JSON.stringify({
          action: 'login',
          username,
          password
        })
      });

      const data = await response.json();

      if (response.ok) {
        // Salvar token de sess√£o e dados do usu√°rio nos cookies e localStorage
        AuthCookies.saveAuth(data.sessionToken, data.user, rememberMe);
        setUser(data.user);
        setIsAuthenticated(true);

        // Disparar evento customizado para notificar outros componentes sobre o login
        window.dispatchEvent(new CustomEvent('userLoggedIn', {
          detail: { user: data.user }
        }));

        return { success: true, user: data.user };
      } else {
        return { success: false, error: data.error };
      }
    } catch (error) {
      return { success: false, error: 'Erro de conex√£o' };
    }
  };

  // Fun√ß√£o para fazer logout
  const logout = async () => {
    try {
      const sessionToken = AuthCookies.getSessionToken();

      if (sessionToken) {
        await fetch('/api/auth', {
          method: 'POST',
          headers: {
            'Content-Type': 'application/json',
          },
          body: JSON.stringify({
            action: 'logout',
            sessionToken
          })
        });
      }

      // Limpar dados dos cookies e localStorage
      AuthCookies.clearAuth();
      FriendsCookies.clearFriendsData(); // Limpar dados dos amigos tamb√©m
      setUser(null);
      setIsAuthenticated(false);

      return { success: true };
    } catch (error) {
      // Mesmo com erro, limpar dados locais
      AuthCookies.clearAuth();
      FriendsCookies.clearFriendsData(); // Limpar dados dos amigos tamb√©m
      setUser(null);
      setIsAuthenticated(false);
      return { success: false, error: 'Erro de conex√£o' };
    }
  };

  // Fun√ß√£o para obter ID do usu√°rio autenticado
  const getAuthenticatedUserId = () => {
    if (isAuthenticated && user) {
      return `auth_${user.username}`;
    }

    // Fallback: tentar obter dos cookies/localStorage
    try {
      const savedUserData = AuthCookies.getUserData();
      if (savedUserData) {
        return `auth_${savedUserData.username}`;
      }
    } catch (error) {
      // Silent error handling
    }

    return null;
  };

  // Fun√ß√£o para obter dados completos do usu√°rio autenticado
  const getAuthenticatedUser = () => {
    if (isAuthenticated && user) {
      return user;
    }

    // Fallback: tentar obter dos cookies/localStorage
    try {
      const savedUserData = AuthCookies.getUserData();
      if (savedUserData) {
        return savedUserData;
      }
    } catch (error) {
      // Silent error handling
    }

    return null;
  };

  // Fun√ß√£o para renovar token automaticamente
  const renewToken = async () => {
    try {
      const sessionToken = AuthCookies.getSessionToken();
      const userData = AuthCookies.getUserData();

      if (!sessionToken || !userData) {
        return { success: false, error: 'Dados de sess√£o n√£o encontrados' };
      }

      const response = await fetch('/api/auth', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        },
        body: JSON.stringify({
          action: 'renew',
          sessionToken,
          username: userData.username
        })
      });

      const data = await response.json();

      if (response.ok) {
        // Atualizar token e dados
        AuthCookies.saveAuth(data.sessionToken, data.user, AuthCookies.shouldRemember());
        setUser(data.user);
        setIsAuthenticated(true);
        console.log('‚úÖ Token renovado com sucesso');
        return { success: true, user: data.user };
      } else {
        console.log('‚ùå Falha ao renovar token:', data.error);
        return { success: false, error: data.error };
      }
    } catch (error) {
      console.log('‚ùå Erro ao renovar token:', error);
      return { success: false, error: 'Erro de conex√£o' };
    }
  };

  // Fun√ß√£o para verificar se usu√°rio est√° logado
  const requireAuth = () => {
    return isAuthenticated && user;
  };

  // Fun√ß√£o para executar diagn√≥stico de autentica√ß√£o
  const runAuthDiagnostic = async () => {
    return await authDiagnostic.runFullDiagnostic();
  };

  const value = {
    user,
    isLoading,
    isAuthenticated,
    register,
    login,
    logout,
    checkSession,
    renewToken,
    getAuthenticatedUserId,
    getAuthenticatedUser,
    requireAuth,
    runAuthDiagnostic
  };

  return (
    <AuthContext.Provider value={value}>
      {children}
    </AuthContext.Provider>
  );
};
