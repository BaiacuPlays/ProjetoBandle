// Contexto de autentica√ß√£o
import React, { createContext, useContext, useState, useEffect } from 'react';
import { AuthCookies, FriendsCookies } from '../utils/cookies';

const AuthContext = createContext();

export const useAuth = () => {
  const context = useContext(AuthContext);
  if (!context) {
    throw new Error('useAuth deve ser usado dentro de um AuthProvider');
  }
  return context;
};

export const AuthProvider = ({ children }) => {
  const [user, setUser] = useState(null);
  const [isLoading, setIsLoading] = useState(true);
  const [isAuthenticated, setIsAuthenticated] = useState(false);

  // Verificar se h√° sess√£o ativa ao carregar
  useEffect(() => {
    if (typeof window !== 'undefined') {
      checkSession();
    }
  }, []);

  // Fun√ß√£o para verificar sess√£o existente
  const checkSession = async () => {
    try {
      // Tentar obter token dos cookies primeiro, depois localStorage
      const sessionToken = AuthCookies.getSessionToken();

      if (!sessionToken) {
        console.log('üîç Nenhum token de sess√£o encontrado nos cookies ou localStorage');
        setIsLoading(false);
        return;
      }

      console.log('üîç Verificando sess√£o existente...');
      const response = await fetch(`/api/auth?sessionToken=${sessionToken}`, {
        method: 'GET',
        headers: {
          'Cache-Control': 'no-cache',
          'Pragma': 'no-cache'
        }
      });

      if (response.ok) {
        const data = await response.json();
        setUser(data.user);
        setIsAuthenticated(true);
        console.log('‚úÖ Sess√£o v√°lida encontrada:', data.user.displayName);

        // Atualizar cookies e localStorage com dados mais recentes se necess√°rio
        const currentUserData = JSON.stringify(data.user);
        const savedUserData = AuthCookies.getUserData();
        if (!savedUserData || JSON.stringify(savedUserData) !== currentUserData) {
          AuthCookies.saveAuth(sessionToken, data.user, AuthCookies.shouldRemember());
        }
      } else {
        // Sess√£o inv√°lida, mas n√£o remover token imediatamente
        // Pode ser um erro tempor√°rio de rede
        const errorData = await response.json().catch(() => ({}));
        console.log('‚ùå Erro na verifica√ß√£o de sess√£o:', response.status, errorData.error);

        // S√≥ remover token em casos espec√≠ficos de sess√£o realmente inv√°lida
        if (response.status === 401 &&
            (errorData.error === 'Sess√£o inv√°lida ou expirada' ||
             errorData.error === 'Sess√£o expirada' ||
             errorData.error === 'Token de sess√£o n√£o fornecido')) {

          console.log('‚ö†Ô∏è Sess√£o possivelmente inv√°lida, mas mantendo dados locais como fallback');

          // Em vez de remover imediatamente, usar dados dos cookies/localStorage como fallback
          const savedUserData = AuthCookies.getUserData();
          if (savedUserData) {
            try {
              setUser(savedUserData);
              setIsAuthenticated(true);
              console.log('üì± Usando dados salvos localmente (sess√£o pode estar temporariamente inv√°lida)');
            } catch (e) {
              console.error('Erro ao usar dados salvos:', e);
              // S√≥ remover se os dados est√£o corrompidos
              AuthCookies.clearAuth();
            }
          } else {
            // S√≥ remover token se n√£o h√° dados salvos
            AuthCookies.clearAuth();
            console.log('‚ùå Nenhum dado salvo encontrado, removendo tokens');
          }
        } else {
          // Para outros erros (500, timeout, etc.), usar dados dos cookies/localStorage como fallback
          const savedUserData = AuthCookies.getUserData();
          if (savedUserData) {
            try {
              setUser(savedUserData);
              setIsAuthenticated(true);
              console.log('üì± Usando dados salvos localmente como fallback (erro tempor√°rio de rede)');
            } catch (e) {
              console.error('Erro ao usar dados salvos:', e);
              // S√≥ remover se os dados est√£o corrompidos
              AuthCookies.clearAuth();
            }
          }
        }
      }
    } catch (error) {
      console.error('Erro ao verificar sess√£o (rede):', error);

      // Em caso de erro de rede, tentar carregar dados dos cookies/localStorage
      const savedUserData = AuthCookies.getUserData();
      if (savedUserData) {
        try {
          setUser(savedUserData);
          setIsAuthenticated(true);
          console.log('üì± Usando dados salvos localmente (erro de rede)');
        } catch (e) {
          console.error('Erro ao usar dados salvos:', e);
          AuthCookies.clearAuth();
        }
      }
    } finally {
      setIsLoading(false);
    }
  };

  // Fun√ß√£o para registrar usu√°rio
  const register = async (username, password, email = null, anonymousUserId = null) => {
    try {
      // Capturar c√≥digo de refer√™ncia da URL se existir
      const urlParams = new URLSearchParams(window.location.search);
      const referralCode = urlParams.get('ref');

      const response = await fetch('/api/auth', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        },
        body: JSON.stringify({
          action: 'register',
          username,
          password,
          email,
          anonymousUserId,
          referralCode
        })
      });

      const data = await response.json();

      if (response.ok) {
        // Salvar token de sess√£o e dados do usu√°rio nos cookies e localStorage
        AuthCookies.saveAuth(data.sessionToken, data.user, true); // Sempre lembrar no registro
        setUser(data.user);
        setIsAuthenticated(true);
        console.log('‚úÖ Usu√°rio registrado:', data.user.displayName);

        // Disparar evento customizado para notificar outros componentes sobre o registro
        window.dispatchEvent(new CustomEvent('userLoggedIn', {
          detail: { user: data.user }
        }));

        // Mostrar mensagem de referral se houver
        if (data.referralMessage) {
          setTimeout(() => {
            alert(`üéâ ${data.referralMessage}`);
          }, 1000);
        }

        return { success: true, user: data.user };
      } else {
        console.error('‚ùå Erro no registro:', data.error);
        return { success: false, error: data.error };
      }
    } catch (error) {
      console.error('‚ùå Erro na requisi√ß√£o de registro:', error);
      return { success: false, error: 'Erro de conex√£o' };
    }
  };

  // Fun√ß√£o para fazer login
  const login = async (username, password, rememberMe = true) => {
    try {
      const response = await fetch('/api/auth', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        },
        body: JSON.stringify({
          action: 'login',
          username,
          password
        })
      });

      const data = await response.json();

      if (response.ok) {
        // Salvar token de sess√£o e dados do usu√°rio nos cookies e localStorage
        AuthCookies.saveAuth(data.sessionToken, data.user, rememberMe);
        setUser(data.user);
        setIsAuthenticated(true);
        console.log('‚úÖ Login realizado:', data.user.displayName);

        // Disparar evento customizado para notificar outros componentes sobre o login
        console.log('üîî Disparando evento userLoggedIn para:', data.user.displayName);
        window.dispatchEvent(new CustomEvent('userLoggedIn', {
          detail: { user: data.user }
        }));

        return { success: true, user: data.user };
      } else {
        console.error('‚ùå Erro no login:', data.error);
        return { success: false, error: data.error };
      }
    } catch (error) {
      console.error('‚ùå Erro na requisi√ß√£o de login:', error);
      return { success: false, error: 'Erro de conex√£o' };
    }
  };

  // Fun√ß√£o para fazer logout
  const logout = async () => {
    try {
      const sessionToken = AuthCookies.getSessionToken();

      if (sessionToken) {
        await fetch('/api/auth', {
          method: 'POST',
          headers: {
            'Content-Type': 'application/json',
          },
          body: JSON.stringify({
            action: 'logout',
            sessionToken
          })
        });
      }

      // Limpar dados dos cookies e localStorage
      AuthCookies.clearAuth();
      FriendsCookies.clearFriendsData(); // Limpar dados dos amigos tamb√©m
      setUser(null);
      setIsAuthenticated(false);
      console.log('‚úÖ Logout realizado - dados de autentica√ß√£o e amigos limpos');

      return { success: true };
    } catch (error) {
      console.error('‚ùå Erro no logout:', error);
      // Mesmo com erro, limpar dados locais
      AuthCookies.clearAuth();
      FriendsCookies.clearFriendsData(); // Limpar dados dos amigos tamb√©m
      setUser(null);
      setIsAuthenticated(false);
      return { success: false, error: 'Erro de conex√£o' };
    }
  };

  // Fun√ß√£o para obter ID do usu√°rio autenticado
  const getAuthenticatedUserId = () => {
    if (isAuthenticated && user) {
      return `auth_${user.username}`;
    }

    // Fallback: tentar obter dos cookies/localStorage
    try {
      const savedUserData = AuthCookies.getUserData();
      if (savedUserData) {
        return `auth_${savedUserData.username}`;
      }
    } catch (error) {
      console.error('Erro ao obter ID do usu√°rio dos dados salvos:', error);
    }

    return null;
  };

  // Fun√ß√£o para obter dados completos do usu√°rio autenticado
  const getAuthenticatedUser = () => {
    if (isAuthenticated && user) {
      return user;
    }

    // Fallback: tentar obter dos cookies/localStorage
    try {
      const savedUserData = AuthCookies.getUserData();
      if (savedUserData) {
        return savedUserData;
      }
    } catch (error) {
      console.error('Erro ao obter dados do usu√°rio dos dados salvos:', error);
    }

    return null;
  };

  // Fun√ß√£o para verificar se usu√°rio est√° logado
  const requireAuth = () => {
    return isAuthenticated && user;
  };

  const value = {
    user,
    isLoading,
    isAuthenticated,
    register,
    login,
    logout,
    checkSession,
    getAuthenticatedUserId,
    getAuthenticatedUser,
    requireAuth
  };

  return (
    <AuthContext.Provider value={value}>
      {children}
    </AuthContext.Provider>
  );
};
